	<p>Utvecklare, nördar och gudar. Trappans webblag har många smeknamn. Och det är just denna samling av människor, alltså Trappans webblag 2014, som tagit fram Personalportalen.<br /><br /> Har du några frågor eller synpunkter angående portalen eller kanske bara känner för att prata med helgrymma människor så går det alldeles utmärkt att kontakta någon i Trappans <a href="">webblag</a>, förslagsvis <a href="">lagchefen</a>.</p>
	<h4>Glyphicons</h4>
	<p>I och med att portalen använder sig av Bootstrap 3.0 har vi haft förmånen att använda oss av ikoner från Glyphicon Halflings set. Detta set är egentligen inte gratis, men har gjorts tillgängliga just för Bootstrap. Så det hör lite till god sed att länka tillbaka till Glyphicons och ge dem cred. Och det gör vi lite här och var. <a href="http://glyphicons.com">Som här till exempel</a>.</p>
	<h4>Gamla Personalportalen</h4>
	<p>Den gamla Personalportalen användes från 2007 fram till 2014 och utvecklades av Johan Åkesson och Tobias Johansson. Många av den gamla portalens funktioner och kod används även i den nya portalen, men mycket har finjusterats, tagits bort och lagts till. Utseendet på den nya portalen skiljer sig även avsevärt.</p>
